def generate_id():
    """Generate a random 16-char hex ID"""
    return ''.join(random.choices(string.hexdigits[:16], k=16))

def fill_template(template, vars_dict):
    """Fill in template variables"""
    result = template
    for var, options in vars_dict.items():
        if f'{{{var}}}' in result:
            result = result.replace(f'{{{var}}}', random.choice(options))
    return result

def generate_examples(category, config, count=100):
    """Generate 'count' email examples for a category"""
    examples = []
    for i in range(count):
        sender = random.choice(config['senders'])
        subject = fill_template(random.choice(config['subject_templates']), config.get('vars', {}))
        snippet = fill_template(random.choice(config['snippet_templates']), config.get('vars', {}))

        examples.append({
            'id': generate_id(),
            'sender': sender,
            'subject': subject,
            'snippet': snippet,
            'category': category,
        })
    return examples

def main():
    output_dir = Path('/home/amcolash/Github/uncluttered/classifier/training')
    output_dir.mkdir(parents=True, exist_ok=True)

    # Generate main training files
    print(f"Generating training data in {output_dir}...")
    total_examples = 0

    for category, config in CATEGORIES.items():
        examples = generate_examples(category, config, 100)
        total_examples += len(examples)

        output_file = output_dir / f'{category}.json'
        with open(output_file, 'w') as f:
            json.dump({'emails': examples}, f, indent=2)
        print(f"✓ {category}: 100 examples")

    # Generate edge cases file
    print("\nGenerating edge cases...")
    edge_cases = []

    # For each category, generate 3-5 ambiguous examples that could fit multiple categories
    ambiguous_pairs = [
        ('PERSONAL', 'RECRUITER'),  # Could be a friend reaching out vs recruiter
        ('NEWSLETTER', 'PROMO_MARKETING'),  # Newsletter vs promotional content
        ('APPOINTMENT_REMINDER', 'EVENT_TICKET'),  # Event reminder vs ticket
        ('FOOD_ORDER', 'SUBSCRIPTION_SERVICE'),  # One-off order vs subscription
        ('FINANCE_BILL', 'SUBSCRIPTION_SERVICE'),  # Invoice vs subscription bill
        ('SYSTEM_ALERT', 'DEVELOPER_SERVICES'),  # System alert vs dev platform notification
        ('BANKING_ACCOUNT', 'FINANCE_BILL'),  # Bank alert vs bill notice
        ('COMMUNITY_LOCAL', 'NONPROFIT_ADVOCACY'),  # Local group vs advocacy org
        ('TRAVEL_BOOKING', 'ORDER_SHIPPING'),  # Travel reservation vs package tracking
    ]

    for cat1, cat2 in ambiguous_pairs:
        # Generate a few examples that could fit either category
        for _ in range(3):
            config1 = CATEGORIES[cat1]
            sender = random.choice(config1['senders'])
            subject = fill_template(random.choice(config1['subject_templates']), config1.get('vars', {}))
            snippet = fill_template(random.choice(config1['snippet_templates']), config1.get('vars', {}))

            edge_cases.append({
                'id': generate_id(),
                'sender': sender,
                'subject': subject,
                'snippet': snippet,
                'category': cat1,
                'note': f'Could also be {cat2}',
            })

    edge_cases_file = output_dir / 'edge_cases.json'
    with open(edge_cases_file, 'w') as f:
        json.dump({'emails': edge_cases}, f, indent=2)

    print(f"✓ Edge cases: {len(edge_cases)} examples")
    print(f"\n✅ Total: {total_examples + len(edge_cases)} training examples generated")
    print(f"📁 Output directory: {output_dir}")

if __name__ == '__main__':
    main()
